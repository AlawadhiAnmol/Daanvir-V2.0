Flush with nearly three times of its usual population, the Swiss resort of Davos is teeming with black business suits for the WEF annual pow-wow, but still cannot dampen the spirit of skiing enthusiasts and those coming for medical tourism here. 
</p><p>
This snow-laden place on the Swiss Alps where temperature can go to as low as minus 20 degrees this season has been home to the annual talkathon of the rich and powerful from across the world for over four decades now.
</p><p>
But it has a much older and fascinating history of its own of being a place of eminence for medical tourism as also for winter sports. 
</p><p></b>
Arthur Conan Doyle, the creator of famous detective character, Sherlock Holmes, moved to this town along with his ailing wife that reportedly helped her live longer.
</p><p>
With the annual 5-day WEF annual meeting 2017 beginning today, the small Alpine town is buzzing with top CEOs and others who matter in black business suits and the police and army people in orange-and-blue dress posted to provide security for such a large-scale event. 
</p><p>
Still, the streets are not totally devoid of usual skiing enthusiasts and some tourists devouring the rich history of this small Swiss town on the snow-capped Alps.
</p><p>
Once famous for being a summer health resort, Davos has gradually emerged as a major winter sport hub on the Alps, but its biggest claim to fame for the past four decades has been World Economic Forum's annual meeting every January, beginning 1971.
</p><p>
The Geneva-based WEF is hosting its 47th annual meeting here beginning today, wherein more than 3,000 leaders from across the world, including 50 heads of government, are expected to indulge in a high-profile talk fest for five days.
</p><p>
To cover this global elite jamboree, there are nearly 1,000 journalists and support staff as well.
</p><p>
While such a high-profile event leads to all hotels and rental apartments being occupied, the die-hard winter sport fans still throng this place as WEF week also means relatively smaller crowds in ski areas and on mountain cableways.
</p><p>
The only drawback for tourists is that they cannot stay within the town, which has less than 10 medium-sized hotels and about 40 small ones including nearby areas like Klosters and Dorf.
</p><p>
Besides, the so-called WAGs (wives and girlfriends of those attending WEF meet) are also around in large numbers on ski circuits and at various tourist destinations of the town that comprises two big parallel roads and numerous connecting alleys.
</p><p>
Davos' history as a modern and popular holiday destination dates back to 150 years ago in 1865 when first winter guests arrived here. Till then, it was just a summer mountain health resort with a strong reputation for treatment of tuberculosis patients.
</p><p>
One day in February 1865, Doctor Friedrich Unger and Hugo Richter from Germany arrived here and began a course of treatment on a bed made from a hay sled covered with boards.
</p><p>
The treatment proved successful and both men were able to return to work. Soon after, Doctor Friedrich Unger returned to Davos and worked as a doctor here for over 20 years.
</p><p>
Hugo Richter married a Davos girl and took over the management of a guest house here. Later, he also moved his publishing business to this small town and began printing two local newspapers.
</p><p>
Another feather in its cap is Davos being home to Ernst Ludwig Kirchner, who spent his last 20 years in this town, which is full of many of his finest paintings.
</p><p>
Besides a museum devoted to Kirchner's work, his paintings can be seen anywhere and everywhere in Davos. Towards the end of his life, Kirchner suffered a major nervous breakdown and spent his last days in a sanatorium in Davos.
</p><p>
This is the same sanatorium that inspired Noble laureate Thomas Mann's classic novel 'The Magic Mountain'.
